#!/usr/bin/env bash
# Opens a clearly-marked TEST proposal in iso24229/proposals to verify
# the end-to-end pipeline (form → issue → AG mirror → import PR).
#
# Requirements:
#   - gh CLI installed and authenticated with scope that can write to
#     iso24229/proposals
#   - The proposals repo + decisions repo must both have
#     ISO24229_CI_PAT_TOKEN set (you confirmed this).
#
# Usage:
#   ./scripts/test-proposal.sh                  # auto-generate test body
#   ./scripts/test-proposal.sh --label ready    # also label status:ready-for-AG
#                                               # (triggers the mirror workflow)
#   ./scripts/test-proposal.sh --clean ISSUE_NUMBER
#                                               # close + comment "TEST, please ignore"
#
# What this script does NOT do:
#   - Label the mirror issue in iso24229/decisions with status:AG-approved
#     (you do that manually so you can verify the import PR step)
#   - Clean up the resulting PR in iso24229/iso24229-register (you do that)

set -euo pipefail

REPO="iso24229/proposals"
TEST_TAG="[TEST — please ignore]"
TODAY=$(date -u +%Y-%m-%d)
NOW=$(date -u +%Y%m%d-%H%M%S)

# Sample proposal — Wade-Giles, which is a real historic Chinese
# romanization, so the schema validates, but flagged TEST so the AG
# knows to skip it. The substantive content matches what a real
# proposal would look like.

TITLE="$TEST_TAG [Proposal] ALA-LC Romanization Table -- Amharic (test $NOW)"

read -r -d '' BODY <<EOF || true
<!-- ISO 24229 system-code proposal generated by www.iso24229.org/about/apply/submit -->
<!-- integrity-sha256: 6f4a02b8c1a13e7b8d9c6e0a6e7b8c9d0e1f2a3b4c5d6e7f8a9b0c1d2e3f4a5 (test value) -->
<!-- TEST: this proposal exists only to verify the end-to-end pipeline. The AG should reject or ignore it. -->

### Your name

Pipeline Test Bot

### Organization (optional)

iso24229 tooling

### Your relationship to the proposed authority

I am proposing for the Varia authority (Var)

### Authority identifier

Var

### Authorization

I confirm I am authorized to submit on behalf of the chosen authority, OR I am proposing for Varia, OR I am proposing a new authority.

### Source language (ISO 639 code)

amh

### Source script (ISO 15924 code)

Ethi

### Source country (optional, ISO 3166-1 alpha-2)

