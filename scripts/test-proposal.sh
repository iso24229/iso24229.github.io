### Justification

This proposal exists only to verify the end-to-end pipeline. It should be rejected by the AG. No register entry should result.

### Example conversions

አማርኛ → amarəñña (test)
መጽሐፍ → mäs'ḥaf (test)

### Relations to other systems (optional)

