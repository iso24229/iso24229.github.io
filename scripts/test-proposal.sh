### Submission terms

- [x] I confirm the information is accurate to the best of my knowledge
- [x] I understand the AG may reject this proposal, and that rejection does not result in a register entry
- [x] I understand this proposal is public, but the AG's deliberation (including voting) is private
- [x] I understand I will be @mentioned on this issue for any RA questions, and that I need GitHub notifications enabled to receive them

---

**TEST**: this proposal exists only to verify the proposal→decision→register import pipeline. Please label it \`status:ready-for-AG\` to exercise the mirror workflow, then label the corresponding mirror in \`iso24229/decisions\` \`status:AG-rejected\` to verify the rejection path (recommended over approval, which would actually create a register entry).
EOF

# ── Parse args ──────────────────────────────────────────────
ACTION="create"
LABEL_READY=false
CLEAN_ISSUE=""

while [[ $# -gt 0 ]]; do
  case "$1" in
    --label-ready|--ready) LABEL_READY=true; shift ;;
    --clean) ACTION="clean"; CLEAN_ISSUE="$2"; shift 2 ;;
    --help|-h)
      sed -n '2,30p' "$0"
      exit 0
      ;;
    *) echo "Unknown arg: $1" >&2; exit 1 ;;
  esac
done

# ── Verify gh auth ──────────────────────────────────────────
if ! gh auth status >/dev/null 2>&1; then
  echo "::error::gh CLI is not authenticated. Run 'gh auth login' first."
  exit 1
fi

if [[ "$ACTION" == "clean" ]]; then
  echo "→ Closing test issue #$CLEAN_ISSUE with cleanup comment"
  gh issue comment "$CLEAN_ISSUE" --repo "$REPO" \
    --body "Pipeline test complete. Closing. — Pipeline Test Bot"
  gh issue close "$CLEAN_ISSUE" --repo "$REPO" --reason "not planned"
  echo "✓ Issue #$CLEAN_ISSUE closed"
  exit 0
fi

# ── Create test issue ───────────────────────────────────────
echo "→ Opening test proposal in $REPO"
ISSUE_URL=$(gh issue create \
  --repo "$REPO" \
  --title "$TITLE" \
  --body "$BODY" \
  --label "type:system-proposal" \
  --label "status:received")

ISSUE_NUMBER=$(basename "$ISSUE_URL")
echo "✓ Opened: $ISSUE_URL"
echo "  Issue number: $ISSUE_NUMBER"
echo "  Title: $TITLE"

if $LABEL_READY; then
  echo "→ Adding status:ready-for-AG label (triggers mirror-to-decisions workflow)"
  gh issue edit "$ISSUE_NUMBER" --repo "$REPO" --add-label "status:ready-for-AG"
  echo "✓ Labelled. Mirror workflow should fire within ~30s."
  echo ""
  echo "Next steps to complete the test:"
  echo "  1. Watch the mirror workflow: https://github.com/$REPO/actions"
  echo "  2. Confirm a corresponding issue appears in iso24229/decisions"
  echo "  3. In decisions, label it status:AG-rejected (recommended over approved,"
  echo "     since approval would actually create a register entry)"
  echo "  4. Verify the rejection email/notification fires back on this proposal"
  echo "  5. Clean up: $0 --clean $ISSUE_NUMBER"
fi

echo ""
echo "Test proposal issue: $ISSUE_URL"
