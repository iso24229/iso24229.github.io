### Target script (ISO 15924 code)

Latn

### Identifying segment

test-$NOW

### System name (human-readable)

$TEST_TAG ALA-LC Romanization Table -- Amharic (test $NOW)

### Abstract

Pipeline verification entry. Not a real proposal. The Amharic test romanization is used purely to exercise the proposal→decision→register import workflow.

### Defining reference — title

